#!/bin/bash
set -o errexit

PARAMS=()
PARAMS+=( $@ )

POSITIONAL=()
while [[ $# -gt 0 ]]
 do
   key="$1"
   case $key in
      -v|--verbose)
      set -x
      VERBOSE="--verbose"
      shift # past argument      
      ;;      
      *)    # unknown option
      POSITIONAL+=("$1") # save it in an array for later
      shift # past argument
      ;;
  esac
done

# restore positional parameters
set -- "${POSITIONAL[@]}"

/usr/sbin/files.sh ${PARAMS[@]}

/usr/sbin/startup.sh ${PARAMS[@]}

/usr/sbin/prepare.sh ${PARAMS[@]}

/usr/sbin/start-oracle.sh ${PARAMS[@]}



